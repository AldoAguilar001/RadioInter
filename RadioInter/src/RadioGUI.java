    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jToggleButton1.isSelected()){
            if (RADIO.getFrequency().equals("FM")){
                RADIO.setButton1(Double.parseDouble(RADIO.getStation()));
            } else {
                RADIO.setButton1AM(Double.parseDouble(RADIO.getStation()));
            }
            jTextField1.setText("SAVED");
            jToggleButton1.setSelected(false);
        } else { 
            if (RADIO.getFrequency().equals("FM")){
                RADIO.setStation(Double.toString(RADIO.getButton1()));
                jTextField1.setText(Double.toString(RADIO.getButton1()));
            } else {
                RADIO.setStation(Double.toString(RADIO.getButton1AM()));
                jTextField1.setText(Double.toString(RADIO.getButton1AM()));
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

